local arynaPoints = 1200
local rybakinaPoints = 800

-- Addition, Subtraction, Multiplication, Division
local total = arynaPoints + rybakinaPoints -- 2000
print("Total:", total) -- Total: 2000

local diff = arynaPoints - rybakinaPoints  -- 400
print("Difference:", diff) -- Difference: 400

local product = arynaPoints * 2            -- 2400
print("Product:", product) -- Product: 2400

local div = arynaPoints / 3                -- 400
print("Division:", div) -- Division: 400

-- Modulo
local remainder = 10 % 3 -- 1
print("Remainder:", remainder) -- Remainder: 1

-- Exponentiation
local power = 2 ^ 3 -- 8
print("Power:", power) -- Power: 8